# Function to color nipples red
def color_nipples(text):
    nipples = ["⣜⣁⣾", "⡤⣥⣙"]
    for n in nipples:
        text = text.replace(n, f"{Fore.RED}{n}{Style.RESET_ALL}")
    return text


